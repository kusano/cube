document.addEventListener("DOMContentLoaded", () => {
    function elem(id) {
        return document.getElementById(id);
    }

    elem("input").focus();

    function log(message) {
        const div = document.createElement("div");
        elem("log").appendChild(div);
        div.textContent = message;
    }

    elem("input").addEventListener("input", () => {
        const input = elem("input").value.trim().replaceAll("‘", "'").replaceAll("’", "'");;

        while (elem("log").firstChild) {
            elem("log").removeChild(elem("log").firstChild);
        }
        elem("time").textContent = "";
        elem("result").textContent = "";
        elem("solution").textContent = "";

        let solution = "";
        if (input.startsWith("https://alg.cubing.net/")) {
            const url = new URL(input);
            const setup = url.searchParams.get("setup");
            if (setup) {
                log(`Setup: ${setup}`);
            }
            solution = url.searchParams.get("alg");
            if (!solution) {
                log("alg not found");
                return;
            }
        } else {
            solution = input;
        }
        solution = solution.replaceAll("_", " ").replaceAll("-", "'");
        log(`Solution: ${solution}`);

        const parsed = [];
        for (const move of solution.split(/ |\/\*.*?\*\//)) {
            if (move.match(/^[UDRLFB](|'|2)$/)) {
                parsed.push(move);
            }
        }
        log(`Parsed solution: [${parsed.join(", ")}]`);

        let time = -1;
        for (const match of solution.matchAll(/\/\*(\d+)\*\//g)) {
            time = Math.max(time, +match[1]);
        }
        log(`Time: ${time}`);

        const normalized = [];
        for (const move of parsed) {
            if (normalized.length==0) {
                normalized.push(move);
            } else {
                const prev = normalized.pop();
                if (prev[0]!=move[0]) {
                    normalized.push(prev);
                    normalized.push(move);
                } else {
                    function power(x) {
                        if (x.length==1) {
                            return 1;
                        } else if (x[1]=="0") {
                            return 0;
                        } else if (x[1]=="2") {
                            return 2;
                        } else if (x[1]=="'") {
                            return 3;
                        }
                    }

                    const n1 = power(prev);
                    const n2 = power(move);
                    const n = (n1+n2)%4;
                    normalized.push(move[0]+["0", "", "2", "'"][n]);
                }
            }
        }
        log(`Normalized solution: [${normalized.join(", ")}]`);

        let result = 0;
        for (const move of normalized) {
            if (move.length==1 || move[1]!="0") {
                result++;
            }
        }
        log(`Result: ${result}`);

        if (time>=0) {
            const min = time/60000|0;
            const sec = (time/1000|0)%60;
            const ms = time%1000;

            function pad(x, d) {
                let r = ""+x;
                while (r.length<d) {
                    r = "0"+r;
                }
                return r;
            }
            elem("time").textContent = `${min}:${pad(sec, 2)}.${pad(ms, 3)}`;
        } else {
            elem("time").textContent = "-";
        }
        elem("result").textContent = ""+result;
        elem("solution").textContent = normalized.join(" ");
    });

    elem("copy").addEventListener("click", async () => {
        elem("copy").setAttribute("disabled", "");
        elem("copy").classList.add("is-loading");

        setTimeout(() => {
            elem("copy").removeAttribute("disabled");
            elem("copy").classList.remove("is-loading");
        }, 300);

        let text = "";
        text += `Time: ${elem("time").textContent}\n`;
        text += `Result: ${elem("result").textContent}\n`;
        text += `Solution: ${elem("solution").textContent}\n`;

        await navigator.clipboard.writeText(text);
    });
});
